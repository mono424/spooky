interface TabProps {
  active: boolean;
  onClick: () => void;
  children: React.ReactNode;
}

const Tab: React.FC<TabProps> = ({ active, onClick, children }) => {
  return (
    <button
      onClick={onClick}
      className={`px-4 py-2 font-medium text-sm transition-all duration-200 border-b-2 ${
        active
          ? 'bg-brand-500/10 text-brand-400 border-brand-500'
          : 'text-text-tertiary border-transparent hover:text-text-secondary hover:border-surface-border'
      }`}
    >
      {children}
    </button>
  );
};

// Simple syntax highlighter using React elements
const highlightLine = (line: string, lang: Language): React.ReactNode => {
  // Keywords by language
  const keywords = lang === 'dart'
    ? ['class', 'final', 'required', 'factory', 'return']
    : ['export', 'const', 'import', 'from', 'type', 'function'];

  // Types
  const types = ['String', 'Map', 'dynamic', 'unknown', 'User', 'Thread'];

  // Split line into tokens
  const tokens: React.ReactNode[] = [];
  let remaining = line;
  let key = 0;

  // Handle comments first (they take precedence)
  if (remaining.includes('//')) {
    const commentIndex = remaining.indexOf('//');
    const beforeComment = remaining.substring(0, commentIndex);
    const comment = remaining.substring(commentIndex);

    if (beforeComment) {
      tokens.push(...tokenize(beforeComment, keywords, types, lang, key));
      key += beforeComment.length;
    }

    tokens.push(
      <span key={key} className="text-gray-500 italic">
        {comment}
      </span>
    );

    return <>{tokens}</>;
  }

  return <>{tokenize(remaining, keywords, types, lang, key)}</>;
};

const tokenize = (
  text: string,
  keywords: string[],
  types: string[],
  lang: Language,
  startKey: number
): React.ReactNode[] => {
  const tokens: React.ReactNode[] = [];
  let key = startKey;

  // Match strings, keywords, types, and other tokens
  const pattern = /'[^']*'|"[^"]*"|\b\w+\b|[^\w\s]+|\s+/g;
  const matches = text.match(pattern) || [];

  matches.forEach((token) => {
    // Strings
    if (token.startsWith("'") || token.startsWith('"')) {
      tokens.push(
        <span key={key++} className="text-orange-300">
          {token}
        </span>
      );
    }
    // Keywords
    else if (keywords.includes(token)) {
      tokens.push(
        <span key={key++} className="text-purple-400 font-bold">
          {token}
        </span>
      );
    }
    // Types
    else if (types.includes(token)) {
      tokens.push(
        <span key={key++} className="text-blue-400">
          {token}
        </span>
      );
    }
    // Special keywords: as, z (for zod), const (after 'as')
    else if (token === 'as' || token === 'z' || token === 'infer' || token === 'parse') {
      tokens.push(
        <span key={key++} className="text-purple-400 font-bold">
          {token}
        </span>
      );
    }
    // Punctuation
    else if (/^[^\w\s]+$/.test(token)) {
      tokens.push(
        <span key={key++} className="text-gray-500">
          {token}
        </span>
      );
    }
    // Everything else (whitespace, identifiers)
    else {
      tokens.push(<span key={key++}>{token}</span>);
    }
  });

  return tokens;
};

export const LanguageOutputTabs: React.FC = () => {
  const [activeTab, setActiveTab] = useState<Language>('typescript');

  const renderCode = (code: string, lang: Language) => {
    const lines = code.split('\n');
    return (
      <div className="space-y-0">
        {lines.map((line, i) => (
          <div key={i} className="flex">
            {/* Line Numbers */}
            <div className="text-text-muted text-right pr-4 pl-2 select-none w-12 shrink-0 text-xs">
              {i + 1}
            </div>
            {/* Code Line */}
            <div className="flex-1 whitespace-pre text-text-secondary text-xs lg:text-sm font-mono">
              {line.length === 0 ? <br /> : highlightLine(line, lang)}
            </div>
          </div>
        ))}
      </div>
    );
  };

  return (
    <div className="border border-surface-border bg-surface rounded-xl overflow-hidden shadow-xl">
      {/* Tab Bar */}
      <div className="flex gap-1 border-b border-surface-border bg-surface-elevated">
        <Tab active={activeTab === 'typescript'} onClick={() => setActiveTab('typescript')}>
          TypeScript
        </Tab>
        <Tab active={activeTab === 'dart'} onClick={() => setActiveTab('dart')}>
          Dart
        </Tab>
        <Tab active={activeTab === 'zod'} onClick={() => setActiveTab('zod')}>
          Zod
        </Tab>
      </div>

      {/* Code Panel */}
      <div className="bg-surface p-6 h-[280px] overflow-auto">
        <div key={activeTab} className="animate-in fade-in duration-200">
          {renderCode(codeExamples[activeTab], activeTab)}
        </div>
      </div>
    </div>
  );
};
